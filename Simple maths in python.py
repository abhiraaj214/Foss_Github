# Static:
x = 5
y = 3

# Dynamic:
# Asking the user for 2 numbers.
x = int(input("\nEnter first number: "))
y = int(input("Enter second number: "))

# Addition
a = x + y
print("\nAddition of two numbers is:", a, "\n")

# Subtraction
s = x - y
print("Subtraction of two numbers is:", s, "\n")

# Multiplication
m = x * y
print("Multiplication of two numbers is:", m, "\n")

# Division
m = x / y
print("Division of two numbers is:", m, "\n")
